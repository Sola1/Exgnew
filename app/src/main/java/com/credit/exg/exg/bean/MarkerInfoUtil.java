public class MarkerInfoUtil implements Serializable {
    private static final long serialVersionUID = 8633299996744734593L;

    private double latitude;//纬度
    private double longitude;//经度
    private String name;//人员姓名
    private String description;//特殊人群分类（刑满释放人员等）
    private int type;////特殊人群分类id

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }


    public MarkerInfoUtil(double latitude, double longitude, String description, String name, int type) {
        super();
        this.latitude = latitude;//纬度
        this.longitude = longitude;//经度
        this.name = name;//人员姓名
        this.description = description;//特殊人群分类（刑满释放人员等）
        this.type = type;//特殊人群分类id
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    //toString方法
    @Override
    public String toString() {
        return "MarkerInfoUtil [latitude=" + latitude + ", longitude=" + longitude + ", name=" + ", imgId="
                + ", description=" + "]";
    }

    //getter setter
    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

}
